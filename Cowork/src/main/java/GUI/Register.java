    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
        errorLabel.setText("");
        String user = inputUser.getText();
        String email = inputEmail.getText();
        String password = inputPassword.getText();
        String confirmPassword = inputConfirmPassword.getText();
        
        if(user.equals("Ingrese el nombre de usuario") || user.isBlank()){
            errorLabel.setText("Error: Ingrese todos los campos del formulario");
        }
        else if(email.equals("Ingrese correo electronico") || email.isBlank()){
            errorLabel.setText("Error: Ingrese todos los campos del formulario");
        }
        else if(password.equals("Ingrese contrase√±a") || password.isBlank()){
            errorLabel.setText("Error: Ingrese todos los campos del formulario");
        }
        else if(password.length() < 8){
            errorLabel.setText("Error: contrase√±a muy corta");
        }
        else if(confirmPassword.equals("Confirmar contrase√±a") || confirmPassword.isBlank()){
            errorLabel.setText("Error: Ingrese todos los campos del formulario");
        }
        else if(!email.contains("@gmail.com") && !email.contains("@hotmail.com") && 
                !email.contains("@correo.unimet.edu.ve") && !email.contains("@outlook.com")){
            errorLabel.setText("Error: correo no valido");
        }
        else if(!password.equals(confirmPassword)){
            errorLabel.setText("Error: confirme la contrase√±a");
        }
        else{
            MongoDatabase dataBase = client.getDatabase("cowork");

            MongoCollection users = dataBase.getCollection("users");
                        
            Bson filter = Filters.eq("email", email);
            
            FindIterable<Document> result = users.find(filter);
            
            boolean canRegister = true;
            for(Document doc: result){                
                String emailDoc = (String) doc.get("email");
                if(emailDoc.equals(email)){
                    errorLabel.setText("Email ya registrado");
                    canRegister = false;
                }                
            }
            
            if(canRegister){                
                Document newUser = new Document("_id", new ObjectId())
                        .append("name", user)
                        .append("email", email)
                        .append("password", password)
                        .append("groups", new ArrayList<Group>())
                        .append("tasks", new ArrayList<Task>());   
                
                users.insertOne(newUser);
                
                User currentUser = new User(user, email, new ArrayList<Group>(), new ArrayList<Task>());
                this.dispose();
                new MainMenu(currentUser).setVisible(true);
            }
            
        }
        
        
        
        
        
        
    }//GEN-LAST:event_registerActionPerformed

